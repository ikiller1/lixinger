public class Avg {
	private double latestVal;
	private double latestValPos;
	private double minVal;
	private double maxVal;
	private double maxPositiveVal;
	private double medianVal;
	private double riskVal;
	private double chanceVal;


	public double getLatestValPos() {
		return latestValPos;
	}

	public void setLatestValPos(double latestValPos) {
		this.latestValPos = latestValPos;
	}

	public double getMinVal() {
		return minVal;
	}

	public void setMinVal(double minVal) {
		this.minVal = minVal;
	}

	public double getMaxVal() {
		return maxVal;
	}

	public void setMaxVal(double maxVal) {
		this.maxVal = maxVal;
	}

	public double getMaxPositiveVal() {
		return maxPositiveVal;
	}

	public void setMaxPositiveVal(double maxPositiveVal) {
		this.maxPositiveVal = maxPositiveVal;
	}

	public double getMedianVal() {
		return medianVal;
	}

	public void setMedianVal(double medianVal) {
		this.medianVal = medianVal;
	}

	public double getRiskVal() {
		return riskVal;
	}

	public void setRiskVal(double riskVal) {
		this.riskVal = riskVal;
	}

	public double getChanceVal() {
		return chanceVal;
	}

	public void setChanceVal(double chanceVal) {
		this.chanceVal = chanceVal;
	}

	public double getLatestVal() {
		return latestVal;
	}

	public void setLatestVal(double latestVal) {
		this.latestVal = latestVal;
	}

}
