public class AvgSet {
	private Avg weightedAvg;
	private Avg equalAvg;
	private Avg median;
	private Avg avg;

	public Avg getWeightedAvg() {
		return weightedAvg;
	}

	public void setWeightedAvg(Avg weightedAvg) {
		this.weightedAvg = weightedAvg;
	}

	public Avg getEqualAvg() {
		return equalAvg;
	}

	public void setEqualAvg(Avg equalAvg) {
		this.equalAvg = equalAvg;
	}

	public Avg getMedian() {
		return median;
	}

	public void setMedian(Avg median) {
		this.median = median;
	}

	public Avg getAvg() {
		return avg;
	}

	public void setAvg(Avg avg) {
		this.avg = avg;
	}
}
